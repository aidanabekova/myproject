#     2)Индекс должен запрашиваться программой при запуске.
#     3)При неверно указанном индексе использовать исключения с подсказкой ввода.
#     4)У функции должен быть один аргумент по умолчанию -  список ten.
def list_index(lst=ten):
    while True:
        try:
            index = input("Ведите индекс (для выхода напиши - 'выход'): ")
            if index == "выход":
                break
            print(lst[int(index)])
        except IndexError:
            print(f"вводите от 0 до {len(lst) -1}")
        except:
            print(f"вводите цифры!")

